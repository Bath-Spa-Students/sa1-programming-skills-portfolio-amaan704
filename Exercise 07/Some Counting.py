# Loop  counts up from 0 to 50 in addition by 1
print("Counting up from 0 to 50 in addition  by 1:")
for i in range(0, 51, 1):
    print(i,end=" ")
print("\n")

# Loop  counts down from 50 to 0  reduction by 1
print("Counting down from 50 to 0  reduction 1:")
for i in range(50, -1, -1):
    print(i,end=" ")
print("\n")

